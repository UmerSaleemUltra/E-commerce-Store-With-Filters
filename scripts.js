// Function to create product cards
function createProductCard(product) {
    return `
        <div class="product-card">
            <h2>${product.name}</h2>
            <p>${product.description}</p>
            <p>Price: $${product.price}</p>
            <p>Category: ${product.category}</p>
            <p>Stock: ${product.stock}</p>
            <button>Add To Card </button>
        </div>
    `;
}

// Function to display products
function displayProducts(products) {
    const productContainer = document.getElementById('product-container');
    productContainer.innerHTML = '';
    products.forEach(product => {
        productContainer.innerHTML += createProductCard(product);
    });
}

// Initial display of all products
displayProducts(ecommerceProducts);

// Function to apply filters
function applyFilters() {
    const categoryFilter = document.getElementById('category').value;
    const stockFilter = document.getElementById('stock').checked;
    const priceFilter = document.getElementById('price').checked;

    let filteredProducts = ecommerceProducts;

    if (categoryFilter !== 'all') {
        filteredProducts = filteredProducts.filter(product => product.category === categoryFilter);
    }

    if (stockFilter) {
        filteredProducts = filteredProducts.filter(product => product.stock < 10);
    }

    if (priceFilter) {
        filteredProducts = filteredProducts.filter(product => product.price < 100);
    }

    displayProducts(filteredProducts);
}

// Event listeners for filters
document.getElementById('category').addEventListener('change', applyFilters);
document.getElementById('stock').addEventListener('change', applyFilters);
document.getElementById('price').addEventListener('change', applyFilters);

